def place(b, x, y, z):
    if blocks.test_for_block(AIR, world(x, y, z)):
        blocks.place(b, world(x, y, z))

interact = 0

def on_item_interacted():
    global interact
    global p0
    if interact == 0:
        p0 = player.position()
        player.say(p0)
        interact = 1
        return

    interact = 0

    p1 = player.position()
    player.say(p1)
    cy = p1.get_value(Axis.Y) # - (dx//4)
    if p0.get_value(Axis.X) > p1.get_value(Axis.X):
        dx = p0.get_value(Axis.X) - p1.get_value(Axis.X)
        cx = (p0.get_value(Axis.X) + p1.get_value(Axis.X)) // 2
    else:
        dx = p1.get_value(Axis.X) - p0.get_value(Axis.X)
        cx = (p1.get_value(Axis.X) + p0.get_value(Axis.X)) // 2
    cz = p1.get_value(Axis.Z)

    radius = dx // 2
    colors = [RED_STAINED_GLASS, ORANGE_STAINED_GLASS, YELLOW_STAINED_GLASS, GREEN_STAINED_GLASS, BLUE_STAINED_GLASS, PURPLE_STAINED_GLASS]
    for i in range(len(coss)):
        for j in range(len(colors)):
            place(colors[j], cx + (radius*coss[i]), cy - j + (radius*sins[i]), cz)

player.on_item_interacted(WOODEN_HOE, on_item_interacted)
